function deepMerge(target, source) {
  const out = Array.isArray(target) ? [...target] : { ...target };
  for (const k of Object.keys(source)) {
    const sv = source[k];
    const tv = out[k];
    if (sv !== null && typeof sv === "object" && !Array.isArray(sv)) {
      out[k] = deepMerge(
        tv && typeof tv === "object" && !Array.isArray(tv) ? tv : {},
        sv
      );
    } else {
      out[k] = sv;
    }
  }
  return out;
}

